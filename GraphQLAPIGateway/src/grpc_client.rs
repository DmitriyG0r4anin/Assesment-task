//   service AirQualityService  →  air_quality_service_client::AirQualityServiceClient
//   service EnergyService      →  energy_service_client::EnergyServiceClient
//   service MotionService      →  motion_service_client::MotionServiceClient
//   service RoomService        →  room_service_client::RoomServiceClient
//
// .NET parallel: the typed client classes Grpc.Tools generates, e.g.
//   AirQualityService.AirQualityServiceClient
