// ============================================================================
// grpc_client.rs — gRPC client wrapper for the DataProcessor services
// ============================================================================
//
// This module wraps the four gRPC service clients generated from
// `proto/parameters.proto` and exposes them through a single `GrpcClient`
// struct that the rest of the application (the GraphQL resolvers) can use.
//
// Purpose and intent (for a .NET developer)
// ─────────────────────────────────────────
// If you come from .NET (Grpc.Net.Client / Grpc.Tools), think of this file as
// a small "service" that holds typed gRPC clients (one per proto service) and
// exposes higher-level, application-friendly methods that return DTOs.
// The GraphQL resolvers call these methods instead of directly manipulating
// the generated proto types.
//
// High-level mapping table
// ────────────────────────
// Concept                      │ C# / .NET                     │ Rust / tonic
// ─────────────────────────────┼───────────────────────────────┼─────────────────────────────
// gRPC channel                 │ GrpcChannel.ForAddress(url)   │ tonic::transport::Channel
// Typed service client         │ AirQualityService.            │ AirQualityServiceClient
//                              │   AirQualityServiceClient     │   <Channel>
// Async method                 │ async Task<T>                 │ async fn → Future<Output=T>
// Error propagation            │ throw / catch                 │ Result<T, E> + ? operator
// Nullable value               │ T? / Nullable<T>              │ Option<T>
// Timestamp (proto WKT)        │ Google.Protobuf.              │ prost_types::Timestamp
//                              │   WellKnownTypes.Timestamp    │
// Timestamp (app layer)        │ DateTime / DateTimeOffset     │ chrono::DateTime<Utc>
//
// Generated code (build.rs / tonic_build)
// ───────────────────────────────────────
// `build.rs` uses `tonic_build` (a Rust build-time helper) to compile the
// `.proto` files into Rust source and places the output into Cargo's OUT_DIR.
// The `include_proto!` macro below then pulls that generated file into this
// crate as the `proto` module. This is similar in purpose to the C# code
// generated by Grpc.Tools (but the file placement and usage are Rust-idiomatic).
//
// Practical notes for a .NET developer reading the code:
// - The generated types (e.g. `AirQualityMessage`) are plain structs produced
//   by `prost` and `tonic`. Optional proto fields become `Option<T>` in Rust,
//   analogous to nullable/reference types in C#.
// - Each `service FooService` in the proto becomes a client type named
//   `foo_service_client::FooServiceClient<Channel>`; compare this to the
//   generated `FooService.FooServiceClient` in C#.
// - This module maps those generated proto types into simple DTOs (Rust
//   structs) so the rest of the app (GraphQL layer) doesn't depend on the
//   generated code shape. In .NET you'd commonly map generated messages into
//   application models (AutoMapper / manual mapping).
//
// Error handling approach
// ───────────────────────
// - Rust uses `Result<T, E>` rather than exceptions. This file uses a boxed
//   error alias (`Box<dyn Error + Send + Sync>`) so different underlying
//   error types (network, codec, domain errors returned by the server) can
//   be returned from the same function. The GraphQL layer converts these into
//   GraphQL errors for clients (see schema.rs).
//
// ============================================================================

use chrono::{DateTime, Utc};
use tonic::transport::{Channel, ClientTlsConfig};
use tracing::instrument;

// Pull in every type generated from the proto file.
// The string "parameters" matches the `package parameters;` declaration in
// the .proto file — that is what prost uses as the file name for the output.
//
// .NET parallel: `using DataProcessor.Presentation.Protos;`
//
// Additional notes for .NET devs:
// - In C# the Grpc.Tools/Google.Protobuf toolchain generates .cs files with
//   message and client types; in Rust `prost`/`tonic_build` produce Rust
//   modules that we include with `include_proto!`.
// - The generated Rust module contains both the message structs (e.g.
//   `AirQualityMessage`) and client modules like
//   `air_quality_service_client::AirQualityServiceClient<Channel>`.
//   When reading the generated code mentally map that client type to the
//   familiar C# typed client `AirQualityService.AirQualityServiceClient`.
//
// Example mental mapping:
// - C#: var channel = GrpcChannel.ForAddress(url);
//       var client = new AirQualityService.AirQualityServiceClient(channel);
// - Rust: build an `Endpoint` from `Channel::from_shared`, call
//   `tls_config(...)` when the URI is https (tonic does not do this
//   automatically for `from_shared`), then `connect().await`.
//         let client = air_quality_service_client::AirQualityServiceClient::new(channel.clone());
//
// See the `GrpcClient` implementation below for the actual ergonomic wrapper
// that constructs these clients and exposes friendly methods returning DTOs.
pub mod proto {
    tonic::include_proto!("parameters");
}

// Bring the four generated service client types into scope.
// Each name follows the proto `service` block name, converted to snake_case
// for the module and PascalCase for the struct:
